export interface Address {
  /**
   * Province
   *
   * @type {string}
   * @memberof Address
   */
  province: string;

  /**
   * City
   *
   * @type {string}
   * @memberof Address
   */
  city: string;

  /**
   * Address (plain text, could include number and floor)
   *
   * @type {string}
   * @memberof Address
   */
  address: string;
}
